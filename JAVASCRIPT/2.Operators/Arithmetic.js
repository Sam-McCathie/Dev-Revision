// used for performing calculations.

// x & y are the operand(s)
let x = 10;
let y = 3;

console.log(x + y); // addition operator
// the above is also called an expression which is something that produces an new value.

console.log(x - y); // subtraction
console.log(x * y); // multiplication
console.log(x / y); // division
console.log(x % y); // Remainder
console.log(x ** y); // exponentiation = x to the power of 1.

// Increment (++)
console.log(++x); // this will log 11

console.log(x++); // this will still log 11 - the incriment will occur in the next instance of x being called.
console.log(x); // however, this will log 12

// Decrement (--)
console.log(--y); // works the same as increment but opposite. The others works also x-- -> x
