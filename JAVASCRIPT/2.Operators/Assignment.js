// Assignment Operators (e.g. a *= 3;)

let x = 10;
let y = 10;
let z = 10;
let a = 10;

// the ++ can only add one.
console.log("x = " + ++x);

// Alternative
y = y + 5;
console.log("y = " + y);

// -> shorthand of above.
z += 5; // very similar to ++z | Imagine the second + is replace by = and after the = is what is added.
console.log("z = " + z);

// multiplication example
a *= 3;
console.log("a = " + a);
