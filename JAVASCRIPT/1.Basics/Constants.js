// Change a variable.
let interestRate = 0.3;
console.log(interestRate);
interestRate = 1;
console.log(interestRate);
// a let variable change.

// a const variable cannot.
const iRate = 1;
iRate = 0.5;
console.log(iRate);
// This will give a TypeError: Assignment to constant variable.
// Const should be assigned to variables that aren't going to change.
