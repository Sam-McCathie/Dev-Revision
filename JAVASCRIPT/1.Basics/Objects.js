// .js Object
let person = {
  name: "Sam",
  age: 25,
};
// name: "Sam" is a key value pair.

console.log(person);
// node Objects

//Ways to change or access properites (name or age)
// Dot notation.
person.name = "Acacia"; // <-- easier
console.log(person.name);

// Bracket notation.
person["name"] = "Frey";
console.log(person["name"]);
// or
console.log(person.name);
