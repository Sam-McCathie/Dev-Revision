// Variables are used to store data in a computers memory temporarily.
// use let to define variables that can change.
let name = "Sam";
console.log(name);

// Content between " " becomes a string - even if it is a number.

/*Naming variables;
- Cannot be a reserved keyword
- Should be meaningful
- Cannot start with a number (1name)
- Cannot contain a space or hypen (-)

- Should use camel case (firstName)
*/

//declare multiple variables on one line.
let firstName, lastName;
console.log(firstName + " " + lastName);
// variables start as undefined.

// best practice is to declare on a single line.
let fName;
let lName;
